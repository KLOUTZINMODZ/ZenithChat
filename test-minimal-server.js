// Teste m√≠nimo do servidor sem depend√™ncias externas
require('dotenv').config();
const express = require('express');
const http = require('http');

console.log('üß™ Testando servidor m√≠nimo...');

const app = express();
const server = http.createServer(app);

app.get('/', (req, res) => {
  res.json({ message: 'Servidor funcionando', timestamp: new Date().toISOString() });
});

const PORT = process.env.PORT || 3001;

server.listen(PORT, () => {
  console.log(`‚úÖ Servidor rodando na porta ${PORT}`);
  console.log(`üåê Acesse: http://localhost:${PORT}`);
  
  // Auto-fechar ap√≥s 3 segundos para teste
  setTimeout(() => {
    console.log('üîå Fechando servidor de teste...');
    server.close();
    process.exit(0);
  }, 3000);
});

server.on('error', (error) => {
  console.log('‚ùå Erro no servidor:', error.message);
  process.exit(1);
});
